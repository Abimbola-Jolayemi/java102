public class CharacterEquivalent {
    public static void main(String[] args) {
        // Display integer equivalents of letters, digits and special characters as on the ASCII table
        System.out.println("Character Integer Equivalents:");
        System.out.println("A: " + (int) 'A');
        System.out.println("B: " + (int) 'B');
        System.out.println("C: " + (int) 'C');
        System.out.println("a: " + (int) 'a');
        System.out.println("b: " + (int) 'b');
        System.out.println("c: " + (int) 'c');
        System.out.println("0: " + (int) '0');
        System.out.println("1: " + (int) '1');
        System.out.println("2: " + (int) '2');
        System.out.println("$: " + (int) '$');
        System.out.println(": " + (int) ':');
        System.out.println("+: " + (int) '+');
        System.out.println("/: " + (int) '/');
        System.out.println("Blank Character (Space): " + (int) ' ');
    }
}